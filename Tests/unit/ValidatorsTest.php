<?php

namespace Tests\Unit;

use App\Exceptions\InvalidDateException;
use App\Validators\DateValidator;
use PHPUnit\Framework\TestCase;

/**
 * @package Tests
 * @group unit
 * @group ready
 */
class ValidatorsTest extends TestCase
{
    protected function setUp(): void
    {
        parent::setUp(); // TODO: Change the autogenerated stub
    }

    /**
     * A basic test example.
     *
     * @return void
     */
    public function testDateValidator()
    {
        $this->assertTrue(DateValidator::validateDate('2021-08-30'));
    }

    /**
     * @throws \App\Exceptions\InvalidDateException
     */
    public function testDateValidatorException()
    {
        $this->expectException(InvalidDateException::class);
        DateValidator::validateDate('2021-13-35');
    }
}
